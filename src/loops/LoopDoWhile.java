package loops;

public class LoopDoWhile {
    public static void main(String[] args) {

        int x = 15;
        int y = 14;

        while(x<y) {
            System.out.println("Hi from while loop...");
        }
        do{
            System.out.println("Hi from do while loop...");

        }while(x<y);

            System.out.println("The end!");
        }
    }

