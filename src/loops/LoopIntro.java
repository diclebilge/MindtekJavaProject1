package loops;

public class LoopIntro {
    public static void main(String[] args) {
        // 500 times
        // execute code block x times
        System.out.println("Java");

        // while
        // while (condition) {a} while the condition is true execute code block inside of { }
        int count = 0;
        while (count < 5) {
            System.out.println(count + "Java");
            count = count ;



        }
    }
}




